// Burger menu
document.querySelector('.burger').onclick = function(){
    document.querySelector('.menu').classList.toggle('menu-show');
}

// Questions
function switchQuestions1(){
    var btn1 = document.querySelector('#plus1');
    var info1 = document.querySelector('#info1');
    var qstn1 = document.querySelector('#qstn1');
    btn1.classList.toggle('close');
    info1.classList.toggle('answer-hide');
    qstn1.classList.toggle('answer-open');
}
document.querySelector('#plus1').onclick = switchQuestions1;

